package com.example.javatest;

public class Example {
    public static void main(String[] args) {
        int a = 20;
        int b = 3;

        System.out.printf("%d + %d = %d%n", a, b, a + b);
        System.out.printf("%d - %d = %d%n", a, b, a - b);
        System.out.printf("%d * %d = %d%n", a, b, a * b);
        System.out.printf("%d / %d = %d%n", a, b, a / b);
        System.out.printf("%d / %f = %f%n", a, (double)b, a / (double)b);
    }
}
